int relaciona(char c, int antiguo){ //Relaciona busca según las columnas de la tabla de funciones
    //	L	l	d	_	.	'	"	todo	E	e	+	-	*	/	%	=	<	>	!	&	|	[	]	(	)	,	;	dif  \b  \n  \t  \0
    int valor = c;

    //qInfo() << ";  Valor en ASCII: " << valor;

    if (valor >= 65 && valor <= 90){ //letras mayúsculas
        //qInfo() << "; letra mayúscula";
        return 0;
    } else if (valor >= 97 && valor <= 122){ //letras minúsculas
        //qInfo() << "; letra minúscula";
        return 1;
    } else if (valor >= 48 && valor <= 57){ //dígitos
        //qInfo() << "; dígito";
        return 2 ;
    } else if (valor == 39){ //comilla simple
        return 5;
    } else if (valor == 34){ //comilla doble
        return 6;
    } else if (valor == 10){ //enter/retorno de carro
        return 31;
    } else if (valor == 9){ //tabulador
        return 32;
    } else if (valor == 32){ //espacios
        return 29;
    } else if (valor == 92){
        return 29;
    }

    switch ( c ){
    case '_': return 3;
    case '.': return 4;
    case 'E': return 8;
    case 'e': return 9;
    case '+': return 10;
    case '-': return 11;
    case '*': return 12;
    case '/': return 13;
    case '%': return 14;
    case '#': return 15;
    case '=': return 16;
    case '<': return 17;
    case '>': return 18;
    case '!': return 19;
    case '&': return 20;
    case '|': return 21;
    case '[': return 22;
    case ']': return 23;
    case '(': return 24;
    case ')': return 25;
    case ',': return 26;
    case ';': return 27;
    }

    return 29; //diferente
}

